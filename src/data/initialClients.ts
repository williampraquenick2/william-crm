// Helper to seed a client with a history of orders
export function seedClientHistory(telefone: string, nome: string, index: number): Client {
  const totaisPorProduto: Record<ProductType, number> = {} as any;
  PRODUCTS.forEach(p => {
    totaisPorProduto[p] = 0;
  });

  const historico = [];
  const purchaseStr = REAL_PURCHASES[telefone];

  if (purchaseStr) {
    // Determine purchase date based on index to distribute states nicely across active/inactive categories
    // Current date is 2026-06-11
    let dateStr = '2026-05-20';
    const rem = index % 4;
    
    if (rem === 0) {
      // Very recent active (June 1 - June 10)
      const day = 1 + (index % 10);
      dateStr = `2026-06-0${day}`;
    } else if (rem === 1) {
      // Active (May 10 - May 28)
      const day = 10 + (index % 19);
      dateStr = `2026-05-${day}`;
    } else if (rem === 2) {
      // Inactive +60 days (March 15 - April 10, past 60 days but under 90)
      const day = 10 + (index % 21);
      dateStr = `2026-03-${day}`;
    } else {
      // Inactive +90 days (January 5 - February 25)
      const month = (index % 2) === 0 ? '01' : '02';
      const day = 10 + (index % 19);
      dateStr = `2026-${month}-${day}`;
    }

    const orderItems: { produto: ProductType; quantidade: number }[] = [];
    const parts = purchaseStr.split(';');
    
    parts.forEach(part => {
      const subParts = part.trim().split(':');
      if (subParts.length === 2) {
        const prod = subParts[0].trim() as ProductType;
        const qty = parseInt(subParts[1].trim(), 10);
        if (PRODUCTS.includes(prod) && !isNaN(qty) && qty > 0) {
          orderItems.push({ produto: prod, quantidade: qty });
          totaisPorProduto[prod] += qty;
        }
      }
    });

    if (orderItems.length > 0) {
      historico.push({
        id: `seed-${telefone}-0`,
        data: dateStr,
        itens: orderItems
      });
    }
  }

  // Sort history by date descending
  historico.sort((a, b) => new Date(b.data).getTime() - new Date(a.data).getTime());
  const ultimaCompra = historico.length > 0 ? historico[0].data : null;

  const totalPedidos = historico.length;
  const totalProdutosComprados = Object.values(totaisPorProduto).reduce((a, b) => a + b, 0);

  return {
    telefone,
    nome: nome === "SEM NOME" ? `SEM NOME (${telefone})` : nome,
    historico,
    ultimaCompra,
    totaisPorProduto,
    totalPedidos,
    totalProdutosComprados
  };
}

export function getSeededClients(): Client[] {
  return RAW_CLIENT_LIST.map((raw, index) => seedClientHistory(raw.telefone, raw.nome, index));
}
